// Dev script: take the browser-scraped real marketplace items
// (scripts/scraped-items.json — exact item URL, real price, listing photo),
// download each photo to public/products/, and write src/data/scraped.ts.
//   node scripts/apply-scraped-items.mjs

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT_DIR = join(ROOT, "public", "products");
const items = JSON.parse(readFileSync(join(ROOT, "scripts", "scraped-items.json"), "utf8"));

const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/126.0 Safari/537.36";

mkdirSync(OUT_DIR, { recursive: true });
const manifest = {};

for (const [id, it] of Object.entries(items)) {
  const ext = new URL(it.img).pathname.endsWith(".webp") ? "webp" : "jpg";
  const file = `${id}.${ext}`;
  try {
    const res = await fetch(it.img, { headers: { "User-Agent": UA } });
    const buf = Buffer.from(await res.arrayBuffer());
    if (!res.ok || buf.length < 3000) throw new Error(`status ${res.status}, ${buf.length} B`);
    writeFileSync(join(OUT_DIR, file), buf);
    manifest[id] = { image: `/products/${file}`, soloPriceKzt: it.soloPriceKzt, externalUrl: it.externalUrl };
    console.log(`✓ ${id} ${it.soloPriceKzt} ₸ — photo ${Math.round(buf.length / 1024)} KB — ${it.sourceTitle}`);
  } catch (e) {
    // keep price + link even if the photo download fails (emoji fallback covers it)
    manifest[id] = { soloPriceKzt: it.soloPriceKzt, externalUrl: it.externalUrl };
    console.log(`✗ ${id} photo failed (${e.message}) — price/link kept, emoji fallback`);
  }
  await new Promise((r) => setTimeout(r, 400));
}

const body =
  "// GENERATED by scripts/apply-scraped-items.mjs — do not edit by hand.\n" +
  "// Real marketplace items (exact URL, real price, listing photo) scraped at\n" +
  "// dev time via a real browser; the app stays offline at runtime (NFR-1).\n" +
  "export const scraped: Record<string, { image?: string; soloPriceKzt?: number; externalUrl?: string }> = " +
  JSON.stringify(manifest, null, 2) +
  ";\n";
writeFileSync(join(ROOT, "src", "data", "scraped.ts"), body);
console.log(`\nWrote ${Object.keys(manifest).length} entries to src/data/scraped.ts`);
